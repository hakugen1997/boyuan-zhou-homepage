const originalText = new WeakMap();
const originalHtml = new Map();

function normalized(text) {
  return text.replace(/\s+/g, " ").trim();
}

function replaceTextNode(node, lang) {
  if (!originalText.has(node)) {
    originalText.set(node, node.nodeValue);
  }

  const source = originalText.get(node);
  if (lang === "en") {
    node.nodeValue = source;
    return;
  }

  const key = normalized(source);
  if (!key || !textTranslations[key]) {
    node.nodeValue = source;
    return;
  }

  const leading = source.match(/^\s*/)?.[0] || "";
  const trailing = source.match(/\s*$/)?.[0] || "";
  node.nodeValue = `${leading}${textTranslations[key]}${trailing}`;
}

function applyHtmlTranslations(lang) {
  Object.entries(htmlTranslations).forEach(([selector, japaneseHtml]) => {
    document.querySelectorAll(selector).forEach((element) => {
      if (!originalHtml.has(element)) {
        originalHtml.set(element, element.innerHTML);
      }
      element.innerHTML = lang === "ja" ? japaneseHtml : originalHtml.get(element);
    });
  });
}

function applyLanguage(lang) {
  document.documentElement.lang = lang === "ja" ? "ja" : "en";
  applyHtmlTranslations(lang);

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
  const nodes = [];
  while (walker.nextNode()) {
    nodes.push(walker.currentNode);
  }
  nodes.forEach((node) => replaceTextNode(node, lang));

  document.querySelectorAll("[data-lang-switch]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langSwitch === lang);
  });
  localStorage.setItem("site-language", lang);
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLanguage = localStorage.getItem("site-language") || "en";
  applyLanguage(savedLanguage);

  document.querySelectorAll("[data-lang-switch]").forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.langSwitch));
  });
});
